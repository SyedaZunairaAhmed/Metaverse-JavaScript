function Lowercase1(){

    let x=prompt("enter your text to convert in LOWERCASE");
    let y = x.toLowerCase();
    document.write(y);
}

function Uppercase1(){
    let x=prompt("enter your text to convert in UPPERCASE");
    let y = x.toUpperCase();
    document.write(y);
}
function Titlecase1(){
    let x=prompt("enter your text to convert in TITLECASE");
    let y = x.split();
    document.write(y);
}